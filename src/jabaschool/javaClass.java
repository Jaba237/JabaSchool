    private void tblEtudiantMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEtudiantMouseClicked
        
            DefaultTableModel maTable = (DefaultTableModel)tblEtudiant.getModel();
            int selectedRows = tblEtudiant.getSelectedRow();
            
            id = parseInt(maTable.getValueAt(selectedRows, 0).toString());
            txtMatricule.setText(maTable.getValueAt(selectedRows, 1).toString());
            txtNom.setText(maTable.getValueAt(selectedRows, 2).toString());
            txtPrenom.setText(maTable.getValueAt(selectedRows, 3).toString());
            txtAdress.setText(maTable.getValueAt(selectedRows, 4).toString());
            txtPB.setText(maTable.getValueAt(selectedRows, 5).toString());
            txtTelephone.setText(maTable.getValueAt(selectedRows, 6).toString());
            
    }//GEN-LAST:event_tblEtudiantMouseClicked

