    public void upDateDB() throws ClassNotFoundException{
        
                try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            sqlConn = DriverManager.getConnection(dbname,username,password);
            pst = sqlConn.prepareStatement("select * from jabaclass");
            
            rs = pst.executeQuery();
            
            ResultSetMetaData stData = rs.getMetaData();
            
            q = stData.getColumnCount();
            
            DefaultTableModel maTable = (DefaultTableModel)tblEtudiant.getModel();
            maTable.setRowCount(0);
            
            while(rs.next()){
                
                Vector dataColn = new Vector();
                
                for(i=1;i<=q;i++){
                    
                    dataColn.add(rs.getInt("id"));
                    dataColn.add(rs.getString("Matricule"));
                    dataColn.add(rs.getString("Nom"));
                    dataColn.add(rs.getString("Prenom"));
                    dataColn.add(rs.getString("Adresse"));
                    dataColn.add(rs.getString("BoitePostal"));
                    dataColn.add(rs.getString("Telephone"));
                }
                maTable.addRow(dataColn);
            
            }    
        
        }catch(SQLException ex){
            
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
